import SwiftUI

extension Font {
    static func light(size: CGFloat) -> Font {
        return .custom("AppleSDGothicNeo-Light", size: size)
    }
    
    static func semiBold(size: CGFloat) -> Font {
        return .custom("AppleSDGothicNeo-SemiBold", size: size)
    }
    
    static func bold(size: CGFloat) -> Font {
        return .custom("AppleSDGothicNeo-Bold", size: size)
    }
    
    static func regular(size: CGFloat) -> Font {
        return .custom("AppleSDGothicNeo-Regular", size: size)
    }
    
    static func medium(size: CGFloat) -> Font {
        return .custom("AppleSDGothicNeo-Regular", size: size)
    }
    
    static func extraBold(size: CGFloat) -> Font {
        return .custom("AppleSDGothicNeoEB00", size: size)
    }
}
